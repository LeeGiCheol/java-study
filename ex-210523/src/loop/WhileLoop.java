package loop;

public class WhileLoop {

	public static void main(String[] args) {
		int number = 0;
		
		while (number != 5) {
			System.out.println("Number : " + number);
			number++;
		}
		
		System.out.println();
		
		for (int i = 0; i < 5; i++) {
			System.out.println("Number : " + i);
		}
		
		
	}
	
}
